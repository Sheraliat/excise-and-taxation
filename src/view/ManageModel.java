    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblModel.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, Phases.ERROR_SELECT_TABLE, Phases.ERROR_MESSAGE, JOptionPane.WARNING_MESSAGE);
            return;
        }
        String sql = "DELETE FROM Model WHERE IDModel = " + Integer.parseInt((String) tblModel.getValueAt(row, 0));
        String[] data = {};
        String s1 = "SELECT Model.IDModel FROM Car INNER JOIN Model ON Car.IDModel = Model.IDModel";
        if (DBHandleData.CheckForDelete(s1, Integer.parseInt((String) tblModel.getValueAt(row, 0)))) {
            JOptionPane.showMessageDialog(this, Phases.DELETE_MODEL_ERROR_EXIST, Phases.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE);
        } else {
            int c = JOptionPane.showConfirmDialog(this, Phases.QUESTION_DELETE, Phases.WARNING_MESSAGE, JOptionPane.OK_CANCEL_OPTION);
            if (c == JOptionPane.OK_OPTION) {
                DBHandleData.UpdateData(sql, data);
                JOptionPane.showMessageDialog(this, Phases.DELETE_SUCCESSFULL, Phases.INFORMATION_MESSAGE, JOptionPane.INFORMATION_MESSAGE);
                DBInitData.initBrandModel(tblModel, ss, ss);
            }

        }

    }//GEN-LAST:event_btnDeleteActionPerformed

