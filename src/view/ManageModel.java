    public void SearchModel() {
        Brand m = (Brand) cbbBrand.getSelectedItem();
        Pattern p = Pattern.compile("\\d+");
        Matcher matcher = p.matcher(txtID.getText());
        String sql = "SELECT  Model.IDModel, Brand.BrandName, Model.ModelName, Model.IDBrand "
                + "FROM  Brand INNER JOIN Model ON Brand.IDBrand = Model.IDBrand "
                + "WHERE 1 = 1 ";
        ss = sql;
        if (!txtID.getText().equals("")) {
            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(this, Phases.INVALID_NUMBER_FORMAT, Phases.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                sql += " and Model.IDModel = " + txtID.getText().trim();
            }
        }
        if (!txtName.getText().equals("")) {
            sql += " and Model.ModelName like '%" + txtName.getText().trim() + "%'";
        }
        if (cbbBrand.getSelectedIndex() != 0) {
            sql += " and Model.IDBrand = " + m.getId();
        }
        DBInitData.initBrandModel(tblModel, sql, ss);
        Tools.hideColumnFrom(tblModel, 3);
        int row = tblModel.getRowCount();
        if (row == 0) {
            JOptionPane.showMessageDialog(this, Phases.SEARCH_NOT_FOUND, Phases.WARNING_MESSAGE, JOptionPane.WARNING_MESSAGE);
        }
    }

