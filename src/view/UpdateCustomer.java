    public void Search() {
        Pattern p = Pattern.compile("\\d+");
        Matcher matcher = p.matcher(txtMakh.getText());
        Matcher matcher1 = p.matcher(txtScmt.getText());
        String sql = "SELECT IDCus, Fullname, CASE Gender WHEN 0 THEN 'Female' ELSE 'Male' END AS Gender , "
                + "Address, Phone, IdentityCard, Email "
                + "FROM Customer WHERE 1 = 1";
        s = sql;
        if (!txtMakh.getText().equals("")) {
            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(this, Phases.INVALID_NUMBER_FORMAT, Phases.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                sql += " and IDCus = " + txtMakh.getText().trim();
            }
        }
        if (!txtTenkh.getText().equals("")) {
            sql += " and Fullname like '%" + txtTenkh.getText().trim() + "%'";
        }
        if (!txtEmail.getText().equals("")) {
            sql += " and Email like '%" + txtEmail.getText().trim() + "%'";
        }
        if (!txtScmt.getText().equals("")) {
            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(this, Phases.INVALID_NUMBER_FORMAT, Phases.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                sql += " and IdentityCard like '%" + txtScmt.getText().trim() + "%'";
            }

        }
        DBInitData.initCustomer(tblCustomer, sql, "SELECT * FROM Customer ");
        int row = tblCustomer.getRowCount();
        if (row == 0) {
            JOptionPane.showMessageDialog(this, Phases.SEARCH_NOT_FOUND, Phases.WARNING_MESSAGE, JOptionPane.WARNING_MESSAGE);
        }
    }

