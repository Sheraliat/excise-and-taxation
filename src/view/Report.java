    public void getProfit(JTable tbl, JLabel l1, JLabel l2) {
        Float total = 0f;
        Float t = 0f;
        for (int i = 0; i < tbl.getRowCount(); i++) {
            total += Float.parseFloat((String) tbl.getValueAt(i, 8));
            t += Float.parseFloat((String) tbl.getValueAt(i, 9));
        }
        l1.setText(Float.toString(total));
        l2.setText(Float.toString(total - t));
    }

