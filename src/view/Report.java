    public void get1stYear() {
        String sql = "SELECT SaleInvoice.IDSale, Customer.Fullname, Car.CarName, "
                + "UserSystem.Fullname AS Admin, PayType.PayName, SaleInvoice.SaleDate, SaleInvoice.LastDate, "
                + "SaleInvoice.Status, SaleInvoice.SellPrice, Car.Price, SaleInvoice.Pay1, SaleInvoice.Date1, "
                + "SaleInvoice.Pay2, SaleInvoice.Date2, SaleInvoice.Pay3, SaleInvoice.Date3, SaleInvoice.Pay4, "
                + "SaleInvoice.Date4, SaleInvoice.Pay5, SaleInvoice.Date5, SaleInvoice.Pay6, SaleInvoice.Date6, "
                + "SaleInvoice.Installment "
                + "FROM Car INNER JOIN SaleInvoice ON Car.IDCar = SaleInvoice.IDCar "
                + "INNER JOIN Customer ON SaleInvoice.IDCus = Customer.IDCus "
                + "INNER JOIN PayType ON SaleInvoice.IDPay = PayType.IDPay "
                + "INNER JOIN UserSystem ON SaleInvoice.IDUser = UserSystem.IDUser "
                + "WHERE DATEPART(YEAR,SaleInvoice.SaleDate) < DATEPART(YEAR,GETDATE()) AND SaleInvoice.Status = 1";
        DBInitData.initCustomer(tblyear1, sql, null);
        Tools.hideColumnFrom(tblyear1, 10);
        getProfit(tblyear1, lblTotalYear1, lblProfit1);

    }

