    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblBrand.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, Phases.ERROR_SELECT_TABLE, Phases.ERROR_MESSAGE, JOptionPane.WARNING_MESSAGE);
            return;
        }
        String sql = "DELETE FROM Brand WHERE IDBrand = " + Integer.parseInt((String) tblBrand.getValueAt(row, 0));
        String[] data = {};
        String s1 = "SELECT Brand.IDBrand FROM Brand "
                + "INNER JOIN Model ON Brand.IDBrand = Model.IDBrand "
                + "INNER JOIN Car ON Model.IDModel = Car.IDModel";
        if (DBHandleData.CheckForDelete(s1, Integer.parseInt((String) tblBrand.getValueAt(row, 0)))) {
            JOptionPane.showMessageDialog(this, Phases.DELETE_BRAND_ERROR_EXIST, Phases.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE);
        } else {
            int c = JOptionPane.showConfirmDialog(this, Phases.QUESTION_DELETE, Phases.WARNING_MESSAGE, JOptionPane.OK_CANCEL_OPTION);
            if (c == JOptionPane.OK_OPTION) {
                DBHandleData.UpdateData(sql, data);
                JOptionPane.showMessageDialog(this, Phases.DELETE_SUCCESSFULL, Phases.INFORMATION_MESSAGE, JOptionPane.INFORMATION_MESSAGE);
                DBInitData.initCustomer(tblBrand, "SELECT * FROM Brand", "SELECT * FROM Brand ");
            }

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

