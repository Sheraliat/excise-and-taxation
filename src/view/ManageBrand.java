    public void Search() {
        Pattern p = Pattern.compile("\\d+");
        Matcher matcher = p.matcher(txtID.getText());
        Matcher matcher1 = p.matcher(txtPhone.getText());
        String sql = "SELECT * FROM Brand WHERE 1 = 1 ";
        if (!txtID.getText().equals("")) {
            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(this, Phases.INVALID_NUMBER_FORMAT, Phases.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                sql += " and IDBrand = " + txtID.getText().trim();
            }
        }
        if (!txtPhone.getText().equals("")) {
            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(this, Phases.INVALID_NUMBER_FORMAT, Phases.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE);
                return;
            } else {
                sql += " and IDBrand = " + txtPhone.getText().trim();
            }
        }
        if (!txtName.getText().equals("")) {
            sql += " and BrandName like '%" + txtName.getText().trim() + "%'";
        }
        if (!txtEmail.getText().equals("")) {
            sql += " and Email like '%" + txtEmail.getText().trim() + "%'";
        }
        DBInitData.initBrandModel(tblBrand, sql, "SELECT * FROM Brand ");
        int row = tblBrand.getRowCount();
        if (row == 0) {
            JOptionPane.showMessageDialog(this, Phases.SEARCH_NOT_FOUND, Phases.WARNING_MESSAGE, JOptionPane.WARNING_MESSAGE);
        }
    }

